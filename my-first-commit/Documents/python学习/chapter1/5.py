# 定义一个字符串类型变量 name
name = "CherryGods"
# 输出　我的名字 CherryGods，请多多关照
print("我的名字 %s，请多多关照" % name)

# 定义一个整数变量　student_no
student_no = 10000
print("我的学号是%010d" % student_no)

# 定义小数　price,weight,money
price = 1.5
weight = 10
money = price * weight
print("单价:%.2f元／斤\t重量:%.2f斤\t\t金额:%.4f元" % (price, weight, money))
# 定义一个小数　输出%号
scale = 100
print("数据比例是 %.0f%%" % scale)
